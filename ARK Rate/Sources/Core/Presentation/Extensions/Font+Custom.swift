import SwiftUI

// MARK: -

extension Font {

    static func customInterThin(size: CGFloat) -> Font {
        Font.custom("inter_thin", size: size)
    }

    static func customInterExtraLight(size: CGFloat) -> Font {
        Font.custom("inter_extralight", size: size)
    }

    static func customInterLight(size: CGFloat) -> Font {
        Font.custom("inter_light", size: size)
    }

    static func customInterRegular(size: CGFloat) -> Font {
        Font.custom("inter", size: size)
    }

    static func customInterMedium(size: CGFloat) -> Font {
        Font.custom("inter_medium", size: size)
    }

    static func customInterSemiBold(size: CGFloat) -> Font {
        Font.custom("inter_semibold", size: size)
    }

    static func customInterBold(size: CGFloat) -> Font {
        Font.custom("inter_bold", size: size)
    }

    static func customInterExtraBold(size: CGFloat) -> Font {
        Font.custom("inter_extrabold", size: size)
    }

    static func customInterBlack(size: CGFloat) -> Font {
        Font.custom("inter_black", size: size)
    }
}
